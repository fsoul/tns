
function show_lang_forw(theList)
{
	if (document.getElementById(theList).disabled == true)
	{
		document.getElementById(theList).disabled = false;
	}
	else
	{
		document.getElementById(theList).disabled = false;
	}
}

function hide_lang_forw(theList)
{       
	
	if (document.getElementById(theList).disabled == true)
	{          
		document.getElementById(theList).disabled = true;	
	}
	else
	{          
		document.getElementById(theList).disabled = true;
	}
}
